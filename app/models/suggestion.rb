class Suggestion < ActiveRecord::Base
  #----------------------------------------------------------------------
  # Associations
  #----------------------------------------------------------------------
  has_many    :products

  #----------------------------------------------------------------------
  # Validations
  #----------------------------------------------------------------------
  validates_presence_of :title
  validates_presence_of :image_path

  #----------------------------------------------------------------------
  # Attributes
  #----------------------------------------------------------------------
  attr_accessible :title,:image_path

  #----------------------------------------------------------------------
  # Class methods
  #----------------------------------------------------------------------

  # Add a new suggestion
  #
  def self.add(attributes)
    create!(attributes)
  end

end
